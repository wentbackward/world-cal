// Generates src/lib/all-timezones.ts from the system IANA tz database.
// Run with: node scripts/gen-timezones.mjs
// Re-run after a system tzdata update to refresh the list.
import fs from 'node:fs';

const TAB = '/usr/share/zoneinfo/zone1970.tab';
const OUT = new URL('../src/lib/all-timezones.ts', import.meta.url);

const region = new Intl.DisplayNames(['en'], { type: 'region' });
const countryOf = (code) => {
  try {
    return region.of(code) || code;
  } catch {
    return code;
  }
};

const cityOf = (tz) => tz.split('/').pop().replace(/_/g, ' ');

// tz -> { tz, label, country, aliases }
const byTz = new Map();

for (const line of fs.readFileSync(TAB, 'utf8').split('\n')) {
  if (!line || line.startsWith('#')) continue;
  const [codes, , tz] = line.split('\t');
  if (!tz) continue;
  const codeList = codes.split(',');
  const country = countryOf(codeList[0]);
  const aliases = codeList.slice(1).map(countryOf);
  byTz.set(tz, { tz, label: cityOf(tz), country, aliases });
}

// Union with everything the JS engine supports, so nothing is missed.
const supported = typeof Intl.supportedValuesOf === 'function' ? Intl.supportedValuesOf('timeZone') : [];
for (const tz of supported) {
  if (byTz.has(tz)) continue;
  const region0 = tz.includes('/') ? tz.split('/')[0] : '';
  byTz.set(tz, { tz, label: cityOf(tz), country: region0 === 'Etc' ? 'UTC' : region0, aliases: [] });
}

const entries = [...byTz.values()].sort(
  (a, b) => a.country.localeCompare(b.country) || a.label.localeCompare(b.label)
);

const body = entries
  .map((e) => {
    const aliases = e.aliases.length ? `, aliases: ${JSON.stringify(e.aliases)}` : '';
    return `  { tz: ${JSON.stringify(e.tz)}, label: ${JSON.stringify(e.label)}, country: ${JSON.stringify(e.country)}${aliases} },`;
  })
  .join('\n');

const out = `// AUTO-GENERATED by scripts/gen-timezones.mjs — do not edit by hand.
// Full IANA timezone list with country names, for the timezone picker.
import type { TimeZoneEntry } from '../types';

export const ALL_TIMEZONES: TimeZoneEntry[] = [
${body}
];
`;

fs.writeFileSync(OUT, out);
console.log(`Wrote ${entries.length} timezones to ${OUT.pathname}`);
